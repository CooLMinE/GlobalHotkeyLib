namespace GlobalHotkeyLib
{
    /// <summary>
    /// Enum with the possible modifier combinations when registering a hotkey.
    /// </summary>
    public enum HotkeyModifiersKeys
    {
        None = 0x0000,
        Alt = 0x0001,
        Control = 0x0002,
        Shift = 0x0004,
        WinKey = 0x0008
    }
}
